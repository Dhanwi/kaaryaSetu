// To add new job
router.post("/jobs", jwtAuth, (req, res) => {
  const user = req.user;

  if (user.type != "recruiter") {
    res.status(401).json({
      message: "You don't have permissions to add jobs",
    });
    return;
  }

  const data = req.body;

  if (!data.jobDescription) {
    return res.status(400).send({ error: "Job description is required" });
  }

  if (!data.companyName) {
    return res.status(400).send({ error: "Company name is required" });
  }

  if (
    data.url &&
    !data.url.startsWith("http://") &&
    !data.url.startsWith("https://")
  ) {
    data.url = "https://" + data.url;
  }

  let job = new Job({
    userId: user._id,
    title: data.title,
    logo: data.logo,
    maxApplicants: data.maxApplicants || null,
    maxPositions: data.maxPositions || null,
    dateOfPosting: new Date(),
    deadline: data.deadline,
    skillsets: data.skillsets,
    url: data.url,
    jobType: data.jobType,
    workType: data.workType,
    duration: data.duration,
    salary: data.salary,
    rating: data.rating,
    jobDescription: data.jobDescription,
    companyName: data.companyName,
    funding: data.funding,
    hrMail: data.hrMail,
    hrContactNumber: data.hrContactNumber,
    prompts: data.prompts,
    hrDetails: data.hrDetails,
  });

  job
    .save()
    .then(() => {
      res.json({ message: "Job added successfully to the database" });
    })
    .catch((err) => {
      res.status(400).json(err);
    });
});

// to get all the jobs [pagination] [for recruiter personal and for everyone]
router.get("/jobs", jwtAuth, (req, res) => {
  let user = req.user;

  let findParams = {};
  let sortParams = { dateOfPosting: -1 }; // Sort by dateOfPosting in descending order

  // Log received query parameters
  console.log("Received query parameters:", req.query);

  // to list down jobs posted by a particular recruiter
  if (user.type === "recruiter" && req.query.myjobs) {
    findParams = {
      ...findParams,
      userId: user._id,
    };
  }

  if (req.query.q) {
    findParams = {
      ...findParams,
      $or: [
        { title: { $regex: new RegExp(req.query.q, "i") } },
        { companyName: { $regex: new RegExp(req.query.q, "i") } },
        { skillsets: { $regex: new RegExp(req.query.q, "i") } },
      ],
    };
  }

  if (req.query.jobType) {
    let jobTypes = [];
    if (Array.isArray(req.query.jobType)) {
      jobTypes = req.query.jobType;
    } else {
      jobTypes = [req.query.jobType];
    }
    findParams = {
      ...findParams,
      jobType: {
        $in: jobTypes,
      },
    };
  }

  if (req.query.workType) {
    let workTypes = [];
    if (Array.isArray(req.query.workType)) {
      workTypes = req.query.workType;
    } else {
      workTypes = [req.query.workType];
    }
    findParams = {
      ...findParams,
      workType: {
        $in: workTypes.map((type) =>
          type === "In Office" ? "InOffice" : type
        ), // Update this line
      },
    };
  }

  if (req.query.salaryMin && req.query.salaryMax) {
    findParams = {
      ...findParams,
      $and: [
        {
          salary: {
            $gte: parseInt(req.query.salaryMin),
          },
        },
        {
          salary: {
            $lte: parseInt(req.query.salaryMax),
          },
        },
      ],
    };
  } else if (req.query.salaryMin) {
    findParams = {
      ...findParams,
      salary: {
        $gte: parseInt(req.query.salaryMin),
      },
    };
  } else if (req.query.salaryMax) {
    findParams = {
      ...findParams,
      salary: {
        $lte: parseInt(req.query.salaryMax),
      },
    };
  }

  if (req.query.duration) {
    findParams = {
      ...findParams,
      duration: {
        $lt: parseInt(req.query.duration),
      },
    };
  }

  if (req.query.asc) {
    if (Array.isArray(req.query.asc)) {
      req.query.asc.map((key) => {
        sortParams = {
          ...sortParams,
          [key]: 1,
        };
      });
    } else {
      sortParams = {
        ...sortParams,
        [req.query.asc]: 1,
      };
    }
  }

  if (req.query.desc) {
    if (Array.isArray(req.query.desc)) {
      req.query.desc.map((key) => {
        sortParams = {
          ...sortParams,
          [key]: -1,
        };
      });
    } else {
      sortParams = {
        ...sortParams,
        [req.query.desc]: -1,
      };
    }
  }

  // Log constructed findParams and sortParams
  console.log("Constructed findParams:", findParams);
  console.log("Constructed sortParams:", sortParams);

  let arr = [
    {
      $lookup: {
        from: "recruiterinfos",
        localField: "userId",
        foreignField: "userId",
        as: "recruiter",
      },
    },
    { $unwind: "$recruiter" },
    { $match: findParams },
  ];

  if (Object.keys(sortParams).length > 0) {
    arr = [
      {
        $lookup: {
          from: "recruiterinfos",
          localField: "userId",
          foreignField: "userId",
          as: "recruiter",
        },
      },
      { $unwind: "$recruiter" },
      { $match: findParams },
      {
        $sort: sortParams,
      },
    ];
  }

  Job.aggregate(arr)
    .then((posts) => {
      if (posts == null) {
        res.status(404).json({
          message: "No job found",
        });
        return;
      }
      res.json(posts);
    })
    .catch((err) => {
      res.status(400).json(err);
    });
});

